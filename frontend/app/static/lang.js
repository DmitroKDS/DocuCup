function translatePage(){
    const elements = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
        acceptNode: function(node) {
          return node.nodeValue.trim() ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT;
        }
    });
    let el;
    while (el = elements.nextNode()) {
        const text = el.nodeValue.trim();
        console.log(text)
        if (text) {
            el.nodeValue = lang[text]?.[langCode] ?? el.nodeValue;
        }
    }

    document.querySelectorAll('[placeholder]').forEach((el) => {
        const text = el.getAttribute('placeholder').trim();
        if (text) {
            el.setAttribute('placeholder', lang[text]?.[langCode] ?? text);
        }
    });
}